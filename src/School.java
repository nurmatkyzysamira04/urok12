public class School {
    private String name;
    private int lesson;
    private int pupils;
    void setName(String name){

        this.name=name;
    }
    String getName(){
        return name;
    }
    void setLesson(int lesson){
        this.lesson=lesson;
    }
    int getLesson(){
        return lesson;
    }
    void setPupils(int pupils){
        this.pupils=pupils;
    }
    int getPupils(){
        return pupils;
    }
}
