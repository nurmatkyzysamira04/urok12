public class Car {
    private String brand;
    private String color;
    private int price;
    void setBrand(String brand){
        this.brand=brand;
    }
    String getBrand(){
        return  brand;
    }
    void setColor(String color){
        this.color=color;
    }
    String getColor(){
        return color;
    }
    void  setPrice(int price){
        this.price=price;
    }
    int getPrice(){
        return price;
    }

}
