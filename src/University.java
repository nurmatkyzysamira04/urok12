public class University {
    private String name;
    private int para;
    private int student;
    void setName(String name){
        this.name=name;
    }
    String getName(){
        return name;
    }
    void setPara(int para){
        this.para=para;
    }
    int getPara(){
        return para;
    }
    void setStudent(int student){
        this.student=student;
    }
    int getStudent(){
        return student;
    }
}


